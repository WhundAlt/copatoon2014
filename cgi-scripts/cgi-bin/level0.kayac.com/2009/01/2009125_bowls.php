<h1 id="entryTtl">【告知】2009/1/25 ごはんと新年会@bowlsやります</h1>
<div class="authorImg">
<a href="http://level0.kayac.com/author/doke" title="dokeの記事一覧"><img src="http://level0.kayac.com/mt-static/support/assets_c/userpics/userpic-4-100x100.png" alt="doke" width="72" height="72" />
