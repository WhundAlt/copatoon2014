<section id="entryBody" class="group"> 
<div class="entryBodyInner group">
<!-- [[[ 記事本文 ]]] --> 

<p>イベントはしないとか言っちゃいましたが、今年はあまり深く考えずイベントをたくさんやろうとおもます。</p>
<p><img src="/images/doke/oshiruko.png" alt="" width="318" height="189" /></p>
<p>まずは新年1発目、ごはんとFlash新年会をやろうと思います。</p>
<p>Flash制作に携わってる方、デザイナー、勉強中、興味がある方、Flashな人と関わりたい方など交流できたらうれしいです。</p>
<p>もちろんFlash以外、Web以外でもかまいません。カヤックに興味ある方とかも大歓迎です。プランナー、プログラマ、絵描き、作曲家など幅広い方のご参加をおまちしてます！</p>

<ul>
    <li>日時：2009/1/25(Sun) 18:00~21:00(予定)</li>
    <li>会場：<a href="" target="_blank">ドンブリカフェ bowls</a>@鎌倉</li>
    <li>会費：5000円(コース料理＋飲み放題)</li>
    <li>定員：~60名</li>
</ul>

<p>会場はおなじみの鎌倉にあるカヤックが経営するドンブリカフェbowls。
ちょうど先月にメニューが新しくなりました。おいしいご飯で1年を楽しく始められたらいいなと思います。</p>
<p>肝心の料理ですが、正月らしい何かを依頼中です。まだ未定ですがおしることかもちっぽいものをお願いする予定です。</p>

<h3>参加方法と注意事項</h3>

<p>参加方法は道家までメールでお申し込みください。下記注意事項をご了承の上フォーマットにしたがってお申し込み願います。なお、定員になり次第、締め切らせていただきます</p>
<ul>
    <li>※お申し込みメール1通につき1人まで。</li>
    ※コース予約となりますのでキャンセルはしないでください。</li>
</li>
<h4>申し込み‘フォーマット</h4>
<p>申し込み先：doke[at]kayac.com</p>
<p>件名：【ごはんと新年会2009】参加申し込み</p>
<ul>
    <li>お名前</li>
    <li>ご連絡先(メールアドレス)</li>
    <li>ひとこと</li>
</ul>

<p>出し物等は決めていませんので、何かアイディアがあれば連絡ください！Wonderflでライブコーディングとかしようかなあ…。</p>
<p>プレゼンテーションなんかも大歓迎です！</p>

<p>それではお待ちしてます！！</p>


