<a name="SkillLvl"></a>
<font size="4"><b>Skill Level Specs</b></font>

<BR>

<div align="center">
<table width="95%"><tr><td>
<font size="2">
Every time you play a skirmish game you receive a skill level for that particular game.  This is represented by the orange number displayed at the end of the game.  The higher the difficulty, the more resources (including the value of buildings and units), and the faster you defeat the enemy, the higher your skill level.  The basic formula for skill level is as follows:

<BR><BR><BR>

<div align="center">

<b>(Resources / Time) x Difficulty</b>

</div>

<BR><BR>

<b>Note:</b> For difficulty, easy = 1, medium = 2, and hard = 3.

</font>
</td></tr></table>
</div>

<BR><BR><BR>











<a name="CompLvl"></a>
<font size="4"><b>Composite Level Specs</b></font>

<BR>

<div align="center">
<table width="95%"><tr><td>
<font size="2">
Every time you play a skirmish game your <a href="#SkillLvl" class="ins">skill level</a> is averaged in to what is called your Composite Level.  This takes all of your previous skill levels and finds the average.  The basic formula for composite level is as follows:

<BR><BR><BR>

<div align="center">

<b>(Skill Level Game 1 + Skill Level Game 2 + .... ) / Number of Games</b>

</div>

<BR><BR>

</font>
</td></tr></table>
</div>

