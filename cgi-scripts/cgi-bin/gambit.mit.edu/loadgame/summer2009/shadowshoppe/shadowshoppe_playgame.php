											<p>With the intention to collect data on how people associated character traits with body shapes, <i>Shadow Shoppe</i> is a casual memory game set in a small town that has lost their shadows. The player takes on the role of a temporary apprentice to the shadowmaker and has to help create and return the shadows to the townspeople. Shadows are created by assigning a trait (or traits) to a projected silhouette; shadows are returned by selecting the right shadow based on the given request.</p>
