If any reindeer crashes into any obstacle he will leave your sleigh team to go and play PS2 with this friends.  (You can use the sleigh to teach pesky Elves and Turkeys not to mess with Santa to score extra points)<BR><BR>
The game ends when there are no more reindeer.  Each level must also be completed before your time runs out or you will also lose a life.<BR><BR>
