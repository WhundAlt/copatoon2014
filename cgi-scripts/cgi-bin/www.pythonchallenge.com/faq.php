<b>What is the Python Challenge?</b>
<br>The Python Challenge is a set of riddles that require a little bit 
of 
Python programming to be solved. The solutions are entered by changing 
the 
address of the page (URL). You get used to the idea pretty fast after 
solving the first few 
levels.

<p><b>What's the purpose of the Python Challenge?</b>
<br>I've written the challenge in hope that it will provide an 
entertaining way to explore the Python Programming Language. It is a way 
to demonstrate the great power of Python's batteries.

<p><b>Where did you get the idea from?</b>
<br>I was inspired while I was playing <a 
href="http://www.notpron.com">notpron</a>. It just occured to me that 
the same style of riddles can serve as a way to discover Python.

<p><b>What is the Python programming language?</b>
<br>From <a href="http://www.python.org">Python website</a>: "Python is 
an 
interpreted, interactive, object-oriented programming language. It is 
often compared to Tcl, Perl, Scheme or Java. Python combines remarkable 
power with very clear syntax. It has modules, classes, exceptions, very 
high level dynamic data types, and dynamic typing."

<p><b>Where can I get Python?</b>
<br>Windows users: <a 
href="http://www.activestate.com/Products/ActivePython/">ActivePython</a> 
(freeware) or <a href="http://www.python.org/download/">Idle 
(opensource)</a><br>
Mac users: <a href="http://homepages.cwi.nl/~jack/macpython/">MacPython 
(opensource)</a><br>
Linux users: Most Linux distributions come with Python installed. Just 
type 
python in the shell.

<p><b>Where can I learn Python?</b>
<br>Start with this excellent <a 
href="http://docs.python.org/tut/tut.html">Python 
Tutorial</a>.

<p><b>Do I have to use Python to solve the riddles?</b>
<br>Most of the riddles can be solved with any language. Currently, only 
riddles 5 and 23 require Python. The hints in all riddles are geared 
towards Python 
programmers.

<p><b>What level of Python knowledge is required for solving the 
riddles?</b>
<br>The riddles are designed to be solvable by Python newcomers and yet 
challenging even for Python experts. If you don't know how to do a
certain thing in Python or you are referenced to an unfamiliar module -
treat it as an opportunity to learn it. Google is your friend.

<p><b>And if I get stuck solving a riddle?</b>
<br>Search for hints in the 

<a href="https://groups.google.com/forum/#!forum/python-challenge">Python Challenge 
forums</a>.
